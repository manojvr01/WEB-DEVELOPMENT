//assignment operator
const a=9099;
const b=9099;
//arithmetic operators
console.log(a+b); 
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);
//increment and decrement operators
let c=10;   //const variables cannot be reassigned, so we use let for c to allow incrementing and decrementing
c++;   
console.log(c);
c--;
console.log(c);
//pre-increment and post-increment
let d=5;
console.log(d);
console.log(++d);
console.log(d);
console.log(d++);
console.log(d);
//pre-decrement and post-decrement
let e=5;
console.log(e);
console.log(--e);
console.log(e);
console.log(e--);
console.log(e);
//exponentiation operator
let f=2;
let g=3;
console.log(f**g); //2 raised to the power of 3
